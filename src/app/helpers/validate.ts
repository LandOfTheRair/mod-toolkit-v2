import { sortBy } from 'lodash';
import { IModKit, ModJSON, ValidationMessageGroup } from '../../interfaces';
import {
  checkAutogenerated,
  checkItemStats,
  checkItemUses,
  checkMapBGMs,
  checkMapNPCDialogs,
  checkMapProperties,
  checkMapSpawners,
  checkNPCs,
  checkNPCUsages,
  checkQuests,
  checkRecipes,
  checkSpawners,
  nonexistentItems,
  nonexistentNPCs,
  nonexistentRecipes,
  validateDialogs,
  validateDroptables,
  validateItems,
  validateNPCs,
  validateQuests,
  validateRecipes,
  validateSpawners,
} from './validators';

export function validationMessagesForMod(
  mod: IModKit,
  json: ModJSON
): ValidationMessageGroup[] {
  const validationContainer: ValidationMessageGroup[] = [
    checkItemStats(mod),
    checkItemUses(mod),
    checkMapNPCDialogs(mod),
    checkNPCUsages(mod),
    checkNPCs(mod),
    checkRecipes(mod),
    checkSpawners(mod),
    checkQuests(mod),
    ...checkMapBGMs(mod, json),
    ...checkMapProperties(mod),
    ...checkMapSpawners(mod),
    validateDialogs(mod),
    validateItems(mod),
    validateDroptables(mod),
    validateNPCs(mod),
    validateQuests(mod),
    validateRecipes(mod),
    validateSpawners(mod),
    nonexistentItems(mod),
    nonexistentNPCs(mod),
    nonexistentRecipes(mod),
    checkAutogenerated(mod),
  ];

  validationContainer.forEach((v) => {
    v.messages = sortBy(v.messages, 'message');
  });

  validationContainer.forEach((v) => {
    if (v.messages.length !== 0) return;

    v.messages.push({
      type: 'good',
      message: 'No abnormalities!',
    });
  });

  return sortBy(validationContainer, 'header');
}

export function numErrorsForMod(mod: IModKit, json: ModJSON): number {
  const validationMessages = validationMessagesForMod(mod, json);

  const numErrors = validationMessages
    .map((v) => v.messages)
    .flat()
    .filter((vdn) => vdn.type === 'error').length;

  return numErrors;
}
